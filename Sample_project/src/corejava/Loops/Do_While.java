package corejava.Loops;

public class Do_While {

	public static void main(String[] args) 
	{
		
		/*
		 * Do-while:--->
		 * 			Do-while work same as while loop only difference
		 * 			it accept iteration once, eventhow Condition return false.
		 */
		
		
		int m=1;
		do {
			System.out.println(m);
			m=m+1;
		} while (m<20);
		
		
		/*
		 * Note:--> We can use do-while in selenium to read data
		 * 			from dynamic webtable...
		 */

	}

}
