package static_methods;

public class Input_Devices
{
	
	/*
	 * Note1:--> if you add pubic modifier to method it
	 * 			can accessable outside package
	 * 
	 * Note2:--> Static is a Access specifier.
	 * 			=> All static methods and variables can call
	 * 				without object creation
	 */
	
	
	public static void keyboard()
	{
		System.out.println("keyboard active");
	}
	
	
	public static void mouse()
	{
		System.out.println("mouse active");
	}

}
